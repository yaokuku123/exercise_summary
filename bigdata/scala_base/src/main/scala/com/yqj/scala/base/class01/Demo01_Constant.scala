package com.yqj.scala.base.class01

object Demo01_Constant {
  def main(args: Array[String]): Unit = {
    // 整型常量
    println(10, 20, 30)
    // 浮点型常量
    println(10.12, 12, 3)
    // 字符串常量
    println("hello world")
    // 字符常量
    println('a')
    // 布尔常量
    println(true, false)
    // 空常量
    println(null)
  }
}
