import copy

# copy.copy(), 切片, 工厂方法都是浅拷贝
if __name__ == '__main__':
    # 简单可变对象
    a = [1, 2, 3]
    b = copy.copy(a)
    print(id(a))  # 140470396479600
    print(id(b))  # 140470396513008

    # 可变对象嵌套可变对象
    a = [1, 2, 3, [4, 5]]
    b = copy.copy(a)
    print(id(a))  # 140575087560256
    print(id(b))  # 140575087465328
    print(id(a[3]))  # 140575087847536
    print(id(b[3]))  # 140575087847536

    # 结论：浅拷贝只能拷贝可变数据类型的最外层对象，而无法拷贝内层的对象。所以只需要对外层对象重新开辟内层空间，内层对象依据执行相同内层地址
