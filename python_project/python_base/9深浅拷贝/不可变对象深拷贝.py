import copy

if __name__ == '__main__':
    # 简单不可变对象
    a = (1, 2, 3)
    b = copy.deepcopy(a)
    print(id(a))  # 140699506804128
    print(id(b))  # 140699506804128

    # 不可变对象嵌套不可变对象
    a = (1, 2, 3, (4, 5))
    b = copy.deepcopy(a)
    print(id(a))  # 140699506796976
    print(id(b))  # 140699506796976
    print(id(a[3]))  # 140699506358864
    print(id(b[3]))  # 140699506358864

    # 结论：对于不可变对象，深拷贝也不能拷贝对象。只能修改引用指针，指向相同内存地址空间的数据
