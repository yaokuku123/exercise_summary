import copy

if __name__ == '__main__':
    # 简单可变对象
    a = [1, 2, 3]
    b = copy.deepcopy(a)
    print(id(a))  # 140418455376832
    print(id(b))  # 140418184866624

    # 可变对象嵌套可变对象
    a = [1, 2, 3, [4, 5]]
    b = copy.deepcopy(a)
    print(id(a))  # 140418184480400
    print(id(b))  # 140418455376832
    print(id(a[3]))  # 140418184833216
    print(id(b[3]))  # 140418184866784

    # 结论：对于可变对象，深拷贝可以对对象完全拷贝，不仅可以拷贝外层对象，也可以拷贝内层对象
